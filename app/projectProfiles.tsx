// ---- Период / даты из шкалы полугодий (0 = 1п 2026 … 11 = 2п 2031) ----
const halfLabel = (i: number) =>
    `${i % 2 ? "2п" : "1п"} ${2026 + Math.floor(i / 2)}`;
const halfStartDate = (i: number) =>
    `01.${i % 2 ? "07" : "01"}.${2026 + Math.floor(i / 2)}`;
const halfEndDate = (i: number) =>
    `${i % 2 ? "31.12" : "30.06"}.${2026 + Math.floor(i / 2)}`;

export function projectPeriod(p: ProfileProject) {
    const s = Math.min(p.startQuarter, 11);
    const e = Math.min(s + Math.max(p.duration, 1) - 1, 11);
    return {
        label: s === e ? halfLabel(s) : `${halfLabel(s)} – ${halfLabel(e)}`,
        start: halfStartDate(s),
        end: halfEndDate(e),
    };
}

